public class Blank {
    public static void main(String[] args) {
        String str1=" ";
        String str2=" ";
    
        if(str1.isBlank()){
            System.out.println("Blank");
        }
        else{
            System.out.println("Not Blank");
        }

       if(str2.isEmpty()){
            System.out.println("Empty");
        }
        else{
            System.out.println("Not Empty");
        }
    }
    
}
